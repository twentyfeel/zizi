package com.twentyfeel.laf.core;

/**
 * ZiziDaylightLaf is a concrete implementation of ZiziLaf
 * providing a "Daylight" themed Look and Feel.
 */
public class ZiziDaylightLaf extends ZiziLaf {

	/**
	 * Returns the name of this Look and Feel.
	 *
	 * @return the name of this Look and Feel
	 */
	@Override
	public String getName() {
		return "Zizi Daylight";
	}

	/**
	 * Returns a short description of this Look and Feel.
	 *
	 * @return a short description of this Look and Feel
	 */
	@Override
	public String getDescription() {
		return "Zizi Daylight Look and Feel";
	}
}
